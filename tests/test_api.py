    def test_large_payload_handling(self, client, monkeypatch, tmp_path):
        """Test handling of large payloads"""
        config_file = tmp_path / "test_config.json"
        config_file.write_text(json.dumps({"channels": []}))
        monkeypatch.setattr("main.CONFIG_FILE", str(config_file))

        # Create large keyword list
        large_keywords = ["keyword"] * 10000
        response = client.post(
            "/api/metadata/transcript/test/test.md/keywords",
            json=large_keywords
        )
        # Should handle gracefully (either accept or reject with proper error)
        assert response.status_code in [200, 404, 413, 422]


