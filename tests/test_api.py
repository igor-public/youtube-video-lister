class TestSecurityValidation(TestConfig):
    """Test security and input validation"""

    def test_path_traversal_protection(self, client):
        """Test path traversal attempts are blocked"""
        malicious_paths = [
            "../../../etc/passwd",
            "..%2F..%2F..%2Fetc%2Fpasswd",
            "test/../../sensitive",
        ]

        for path in malicious_paths:
            response = client.get(f"/api/transcript/{path}")
            assert response.status_code in [403, 404]

    def test_invalid_json_handling(self, client):
        """Test invalid JSON in POST requests"""
        response = client.post(
            "/api/channels",
            data="invalid json",
            headers={"Content-Type": "application/json"}
        )
        assert response.status_code == 422  # Unprocessable Entity

    def test_missing_required_fields(self, client, monkeypatch, tmp_path):
        """Test POST with missing required fields"""
        config_file = tmp_path / "test_config.json"
        config_file.write_text(json.dumps({"channels": []}))
        monkeypatch.setattr("main.CONFIG_FILE", str(config_file))

        # Missing required 'url' field
        response = client.post("/api/channels", json={"days_back": 7})
        assert response.status_code == 422


